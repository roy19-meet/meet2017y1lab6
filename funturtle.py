import turtle
turtle.shape('turtle')
square=turtle.clone()
square.shape('square')
square.goto(0,100)
square.goto(100,100)
square.goto(100,0)
square.goto(0,0)
triangle=turtle.clone()
triangle.shape('classic')
triangle.penup()
triangle.goto(100,200)
triangle.pendown()
triangle.goto(0,200)
triangle.goto(50,250)
triangle.goto(100,200)
square.penup()
square.goto(-150,-100)
square.stamp()
square.goto(-200,-100)
triangle.penup()
triangle.goto(-200,200)
triangle.stamp()
triangle.goto(-200,150)


turtle.mainloop()
